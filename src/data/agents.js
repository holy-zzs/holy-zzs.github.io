/** 根据 ID 获取单个智能体 */
export function getAgent(id) {
  return AGENTS.find(a => a.id === id)
}

/** 根据 ID 数组获取多个智能体（过滤无效ID） */
export function getAgents(ids) {
  return ids.map(getAgent).filter(Boolean)
}

/** 按学段筛选智能体 */
export function getAgentsByGrade(grade) {
  return AGENTS.filter(a => a.grade === grade)
}

/** 按职能类别筛选智能体 */
export function getAgentsByCategory(category) {
  return AGENTS.filter(a => a.category === category)
}

/** 按大学学科筛选智能体（需有 subjects 数组字段） */
export function getCollegeAgentsBySubject(subject) {
  return AGENTS.filter(a =>
    a.grade === 'college' && a.subjects && (a.subjects.includes(subject) || a.subjects.includes('all'))
  )
}

/** 按新职能类别筛选智能体（同 getAgentsByCategory，语义化别名） */
export function getAgentsByNewCategory(categoryKey) {
  return AGENTS.filter(a => a.category === categoryKey)
}

/** 获取所有新智能体（排除12个AI创作伙伴） */
export function getNewAgents() {
  return AGENTS.filter(a => !_LEGACY_IDS.includes(a.id))
}

