// 获取教材信息（兜底用演示数据）
function getMaterialInfo(material) {
  if (!material) return DEMO_MATERIAL
  return {
    filename: material.filename || '教材.txt',
    topic: material.structure?.[0]?.title || material.concepts?.[0]?.name || '本教材',
    concepts: (material.concepts || []).slice(0, 6).map(c => c.name),
    formulas: (material.formulas || []).slice(0, 4).map(f => f.latex),
    preview: (material.rawText || '').slice(0, 120)
  }
}

